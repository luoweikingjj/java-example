package com.example.basic;

/**
 * 常量demo
 */
public class ConstantDemo {
    public static void main(String[] args) {
        //输出整数常量
        System.out.println(123);
        //输出小数常量
        System.out.println(0.125);
        //输出字符常量
        System.out.println('A');
        //输出布尔常量
        System.out.println(true);
        //输出字符串常量
        System.out.println("你好Java");
    }
}
